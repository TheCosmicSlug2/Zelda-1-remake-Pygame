"""
Dictionnaire pour transitions entre niveaux
"""

def creer_dic_scrollage() -> dict[int, list[lambda: bool, int]]:
    
    dic_scrollage = {}
    done = False
    # Boucle principale
    for level_idx, level in global_dic_map_data.items():
        try:
            liste_scrollage = []
            done = False
            # En bas
            for content_idx in level[-1]:
                if content_idx < 20:
                    liste_scrollage.append(level_idx + 16)
                    done = True
                    break
            if not done:
                liste_scrollage.append(None)

            #  gauche
            done = False
            left = [row[0] for row in level]
            for content_idx in left:
                if content_idx < 20:
                    liste_scrollage.append(level_idx - 1)
                    done = True
                    break
            if not done:
                liste_scrollage.append(None)

            # en haut
            done = False
            for column in level[0]:
                if column < 20:
                    liste_scrollage.append(level_idx - 16)
                    done = True
                    break
            if not done:
                liste_scrollage.append(None)

            #  droite
            done = False
            right = [row[-1] for row in level]
            for column in right:
                if column < 20:
                    liste_scrollage.append(level_idx + 1)
                    done = True
                    break
            if not done:
                liste_scrollage.append(None)

            dic_scrollage[level_idx] = liste_scrollage

            # Ajouter au dictionnaire
        except:
            dic_scrollage[level_idx] = [None, None, None, None]

    return dic_scrollage


global_dic_scrollage =  creer_dic_scrollage()

print(f"\n[GAME_DATA] : Dictionnaire de scrollage cr")



