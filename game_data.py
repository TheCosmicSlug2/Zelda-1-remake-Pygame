def create_every_ennemy_pos_in_game(nb_min_per_level: int, nb_max_per_level: int) -> dict[int, dict[int, tuple[str, tuple[int, int]]]]:

    """
    Renvoie le dictionnaire des positions et types de tous les ennemis dans le jeu sous la forme :

    {
    ...
    (niveau_précédent),
    ...
    {
        "idx_du_niveau": {
                ...
                (ennemi_précédent),

                "idx_de_l'ennemi_dans_le_niveau": ("type_de_l'ennemi", ("spawn_x", "spawn_y")),

                (ennemi_suivant),
                ...

            }
    }
    ...
    (niveau_suivant),
    ...
    }
    """


    dic_ennemy_pos = {}

    for level_id in dic_world.keys():

        # Trouver le type d'ennemi

        liste_ennemy_type = ["octorok_rouge", "octorok_bleu", "leever_rouge", "leever_bleu", "bat"]

        # 1 type par niveau
        ennemy_type = random.choice(liste_ennemy_type)

        nb_ennemy = random.randint(nb_min_per_level, nb_max_per_level)

        level_ennemy_pos = {}

        for ennemy_idx in range(nb_ennemy):

            starting_pos = (random.randint(0, 15), random.randint(0, 10))
            while dic_world[level_id][starting_pos[1]][starting_pos[0]] > 50:
                starting_pos = (random.randint(0, 15), random.randint(0, 10))

            level_ennemy_pos[ennemy_idx] = (ennemy_type, starting_pos)
        
        dic_ennemy_pos[level_id] = level_ennemy_pos
    
    return dic_ennemy_pos


